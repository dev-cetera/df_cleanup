import 'dart:async' show FutureOr;

import 'package:df_type/df_type.dart' show SequentialController;
import 'package:flutter/foundation.dart' show protected;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

mixin CloseListsMixin {
  late final Iterable<dynamic> _closeList;

  void initCloseList() {
    _closeList = toCloseList();
  }

  Iterable<dynamic> toCloseList();

  @protected
  FutureOr<void> closeAll() {
    final sc = SequentialController<void>();
    for (final resource in _closeList) {
      try {
        sc.add((_) => resource.close());
      } on NoSuchMethodError catch (e) {
        sc.addException(e);
      }
    }
    return sc.complete();
  }
}
